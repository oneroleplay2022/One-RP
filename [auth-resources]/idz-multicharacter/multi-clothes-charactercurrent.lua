loadClothing = function(ped, skinData)
    local drawableNames = {"face", "masks", "hair", "torsos", "legs", "bags", "shoes", "neck", "undershirts", "vest", "decals", "jackets"}
    local propNames = {"hats", "glasses", "earrings", "mouth", "lhand", "rhand", "watches", "braclets"}
    local headOverlays = {"Blemishes","FacialHair","Eyebrows","Ageing","Makeup","Blush","Complexion","SunDamage","Lipstick","MolesFreckles","ChestHair","BodyBlemishes","AddBodyBlemishes"}
    local faceFeatures = {"Nose_Width","Nose_Peak_Hight","Nose_Peak_Lenght","Nose_Bone_High","Nose_Peak_Lowering","Nose_Bone_Twist","EyeBrown_High","EyeBrown_Forward","Cheeks_Bone_High","Cheeks_Bone_Width","Cheeks_Width","Eyes_Openning","Lips_Thickness","Jaw_Bone_Width","Jaw_Bone_Back_Lenght","Chimp_Bone_Lowering","Chimp_Bone_Lenght","Chimp_Bone_Width","Chimp_Hole","Neck_Thikness"}
    
    local drawables = skinData["drawables"]
    local drawTextures = skinData["drawtextures"]
    local props = skinData["props"]
    local propTextures = skinData["proptextures"]

    local headBlend = skinData["headBlend"]
    local hairColor = skinData["hairColor"]
    local headStructure = skinData["headStructure"]
    local headOverlay = skinData["headOverlay"]
    local tattoos = skinData["tattoos"]
    local tattooHashList = createHashList()

    for i = 1, #drawableNames do
        if drawables[0] == nil then
            if drawableNames[i] == "undershirts" and drawables[tostring(i-1)][2] == -1 then
                SetPedComponentVariation(ped, i-1, 15, 0, 2)
            else
                SetPedComponentVariation(ped, i-1, drawables[tostring(i-1)][2], drawTextures[i][2], 2)
            end
        else
            if drawableNames[i] == "undershirts" and drawables[i-1][2] == -1 then
                SetPedComponentVariation(ped, i-1, 15, 0, 2)
            else
                SetPedComponentVariation(ped, i-1, drawables[i-1][2], drawTextures[i][2], 2)
            end
        end
    end

    for i = 1, #propNames do
        local propZ = (drawables[0] == nil and props[tostring(i-1)][2] or props[i-1][2])
        ClearPedProp(ped, i-1)
        SetPedPropIndex(ped, i-1, propZ, propTextures[i][2], true)
    end

    if (headBlend) then 
        SetPedHeadBlendData(ped,
            tonumber(headBlend['shapeFirst']),
            tonumber(headBlend['shapeSecond']),
            tonumber(headBlend['shapeThird']),
            tonumber(headBlend['skinFirst']),
            tonumber(headBlend['skinSecond']),
            tonumber(headBlend['skinThird']),
            tonumber(headBlend['shapeMix']),
            tonumber(headBlend['skinMix']),
            tonumber(headBlend['thirdMix']),
        false)
    end

    if (headStructure) then
        for i = 1, #faceFeatures do
            SetPedFaceFeature(ped, i-1, headStructure[i])
        end
    end

    if (hairColor) then
        SetPedHairColor(ped, tonumber(hairColor[1]), tonumber(hairColor[2]))
    end

    if (headOverlay) then
